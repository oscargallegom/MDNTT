<h2 class="header_left_content">What is Maryland&rsquo;s Trading Program?</h2>
</td></tr>

<tr><td>
<p class= "text_left_content">Maryland&rsquo;s Nutrient Trading Program is a public marketplace for the buying and selling of nutrient (nitrogen and phosphorous) credits.  The purpose of the Program ranges from being able to offset new or increased discharges to establishing economic incentives for reductions from all sources within a watershed and achieving greater environmental benefits than through existing regulatory programs.
</p>

<p class= "text_left_content">In January 2008, the Maryland Department of the Environment (MDE) issued a policy document entitled 
<a href="http://www.mde.state.md.us/Water/nutrientcap.asp">&ldquo;Maryland Policy for Nutrient Cap Management and Trading in Maryland&rsquo;s Chesapeake Bay Watershed&rdquo;</a> that represented the initial phase of the State&rsquo;s policy development on nutrient trading (referred to as Phase I).  That document described the purpose and form of nutrient trading in Maryland and set forth the fundamental principles and guidelines for Maryland&rsquo;s trading programs.  It also spelled out the requirements and procedures for nutrient trades between wastewater treatment plants (point source-point source trading).  With the issuance of this policy, wastewater treatment plants can begin requesting approval for trades with other wastewater treatment plants.    
</p>

<p class= "text_left_content">Phase II was initiated by the Maryland Department of Agriculture (MDA) in the fall of 2007 to address the opportunity for trading between point and nonpoint sources, such as agricultural operations.  The requirements and procedures for point&mdash;nonpoint agricultural trading were issued in April 2008 in two draft documents: <i>The Maryland Policy for Nutrient Cap Management and Trading in Maryland&rsquo;s Chesapeake Bay Watershed </i><i><a href="http://www.mdnutrienttrading.com/docs/Phase%20II-A_Crdt%20Generation.pdf">Phase II A&mdash;Guidelines for the Generation of Agricultural Nonpoint Nutrient Credits</a></i>, and <i><a href="http://www.mdnutrienttrading.com/docs/Phase%20II-B_Crdt%20Purchase.pdf">Phase II B&mdash;Guidelines for Agricultural Nonpoint Credit Purchases.</a></i>  Specifically, these documents provide a mechanism for generating credits from agricultural sources that can be purchased by point sources and other interested buyers, and they describe how credits will be exchanged between buyers and sellers.
</p>

<p Align="center" class= "text_left_content"><b><a name="PointSource">Point Source Trading</a></b>
</p>

<p class= "text_left_content">The key principles of the <a href="http://www.mde.state.md.us/Water/nutrientcap.asp">Phase I - Point Source Trading policy</a> are basic. They call for all new and expanded point source nutrient loads to be fully offset, require all trades to be consistent with local county water and sewerage plans, but  do not allow trading in lieu of required Enhanced Nutrient Removal (ENR) upgrades.  In addition, point source trades will be implemented and enforced via National Pollutant Discharge Elimination System (NPDES) permits, and trades have to be consistent with Total Maximum Daily Loads or TMDLs.  The cornerstone of the policy&rsquo;s key principles is that trades must protect local water quality as well as provide adequate public outreach/stakeholder participation.  Phase I Policy also addressed point source baseline requirements. 
</p>


<p class= "text_left_content">Trading is structured through &ldquo;Units of Trade&rdquo; called a credit, which is equal to one pound of nitrogen or phosphorus delivered to the main stem of the Bay per year.  Credits will be traded within defined trading areas.  At this time, these areas are defined as: the Potomac basin; the Patuxent basin; and everywhere else within the state.
</p>

<p class= "text_left_content">Credits can be generated in a variety of ways. These include upgrading an existing minor wastewater treatment plant to BNR (Biological Nutrient Removal) or ENR status.  Also, an existing wastewater plant can be retired and have its flow sent to a BNR or ENR facility or an existing Onsite Sewage Disposal System (OSDS) can be retired by the connection to public sewer or cluster treatment.  ENR facilities may generate point sources discharge credits by reducing effluent concentration or maintaining flow at less than the design flow basis of the waste load allocation.  In addition, a facility may opt to have land application of wastewater with pretreatment and nutrient management controls.
</p>

<p class= "text_left_content">There is a way, too, for an Onsite Sewage Disposal System (OSDS) to earn nitrogen credits.  The credit calculation for an OSDS assumes hookup to a WWTP discharging nitrogen at 4 mg/L.  If the System is within the Critical Area the potential credits are 12.2 lbs/yr; if it is within 1,000 feet of any perennial surface water the credit would be 7.5 lbs/yr; and then 4.6 lbs/yr everywhere else. It should be noted that commercial OSDS credits will be higher and determined on a case-by-case basis.&nbsp;
</p>

<p class= "text_left_content">One of many important elements is the length of time a trade will last &ndash; or its &ldquo;trade duration.&rdquo; The wastewater treatment plants will strive to buy credits to offset growth. The agreements they make will be a contractual arrangement that will last for 10 years. During this time they will also develop a plan that will put them into a position to acquire the credits for the next 10 years after the initial contract has expired.
</p>

<p  align="center" class= "text_left_content"><a name="NonPointSource"><b>Nonpoint Source Trading</b></a></p>

<p  class= "text_left_content">Maryland&rsquo;s Phase II Trading Program&rsquo;s is directed towards the inclusion of agriculture into the process. That is where point source and agricultural nonpoint source can start trading.  Policies have been developed that define two basic components of this phase of the program. They provide guidelines for the 
<a href="http://www.mdnutrienttrading.com/docs/Phase%20II-A_Crdt%20Generation.pdf">Generation of Agricultural Nonpoint Credits</a> (selling credits), and guidelines for the 
<a href="http://www.mdnutrienttrading.com/docs/Phase%20II-B_Crdt%20Purchase.pdf">Exchange of Agricultural Nonpoint Credits</a> (buying credits).
</p>

<p class= "text_left_content">One of the key principles established for Agricultural Nonpoint Source Credit Generation is that agricultural operations must first meet baseline requirements before generating credits.  Farm operations must meet the level of nutrient reduction called for in the <a href="http://www.dnr.state.md.us/Bay/tribstrat">Tributary Strategy</a> for their basin.  Where a TMDL is required, they must meet the level of reduction prescribed in the related documents.  Additionally, credit generators must be in compliance with all federal, state and local laws and regulations.
</p>

<p class= "text_left_content">For agricultural operations, certain land use (such as crop conversions) and agronomic practices (cover crops, reduced fertilizer application, and manure export), along with structural BMPs (riparian buffers, livestock fencing, etc.) are eligible to produce credits.  To qualify, these practices must be certified and inspected.  They also must be built and operated according to USDA/NRCS specifications. Each practice will have an annual verification carried out by technically competent reviewers who may be representing the state, a third party, or the seller.  This verification will insure that the BMP or practice is being maintained in accordance with the specifications.  Credits will only be generated once the BMP is installed and functioning as designed and approved.  Landowners or farmers cannot use federal or state cost-share funds to generate credits and cannot generate credits by retiring farmland from active production.  All reductions must result in a net decrease in nutrient loads entering the water and be consistent with Phase II trading policy requiring a 10% retirement ratio.
</p>

<p class= "text_left_content">To participate in the Agricultural Nonpoint Source component of Maryland&rsquo;s Nutrient Trading Program, landowners, farmers, or aggregators will utilize a web-based nutrient trading application tool to calculate baseline eligibility and credit potential. Local Soil Conservation Districts can help in accessing the tool and providing assistance to those interested in pursuing the process.  Once credits have been certified and approved, they can be posted on the Maryland Nutrient Trading Program&rsquo;s <a href="http://nutrientnet.mdnutrienttrading.com/marketplace/list.app">Marketplace</a>.  In the Marketplace participants can post and exchange information with potential buyers on credit availability, credits desired, quantity, and price.  To enable effective management and communication of Maryland&rsquo;s Nutrient Trading Program, the Marketplace will include a <a href="http://nutrientnet.mdnutrienttrading.com/trade/list.app">Trading Registry</a> that will catalogue all registered credits and approved trades. 
</p>
</td></tr>


