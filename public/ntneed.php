<p class="text_left_content">Nutrient trading is an innovative approach to help offset new or increased discharges, and offers an interesting alternative for achieving greater environment protection than through existing regulatory programs. <a href="http://www.mde.state.md.us/">The Maryland Department of the Environment</a> (MDE) is a lead agency for the state in the development of a nutrient trading policy. The final policy will address both point source and nonpoint source trades. MDE is also working on an Accounting for Growth process as required by the State&rsquo;s Watershed Implementation Plan. To restore the Bay, Maryland (and other Bay states) not only needs to reduce its nutrient load, but also hold the line against new pollution. Please click <a href="http://www.mde.state.md.us/programs/Water/TMDL/TMDLImplementation/Pages/Accounting_For_Growth.aspx">here</a> for more information on Accounting for Growth and MDE&rsquo;s Guidance on Nutrient Cap Management and Trading in Maryland&rsquo;s Chesapeake Bay Watershed. MDE&rsquo;s guidance establishes an approach for trading between point sources and trading involving the removal of onsite sewage disposal systems (OSDSs). It establishes definitions, key principles, and fundamentals that are applicable to trading programs involving both point sources and nonpoint sources.</p>

<p class="text_left_content">The <a href="http://www.mda.state.md.us/">Maryland Department of Agriculture</a> (MDA) is a lead agency for the development of policy on generation and trade of agricultural nonpoint source credits.  In April 2008, the MDA finalized a draft set of policy documents that represents the next phase of Maryland&rsquo;s Nutrient Trading Program. This Phase II of the program addresses how diffuse sources (non-point source) can be part of a trading program.  Specifically, it provides a mechanism for <a href="http://www.mdnutrienttrading.com/docs/Phase%20II-A_Crdt%20Generation.pdf">generating credits</a> for trading from agricultural sources that can be purchased by point sources and other <a href="http://www.mdnutrienttrading.com/docs/Phase%20II-B_Crdt%20Purchase.pdf">interested buyers</a> and explains how credits will be exchanged between buyers and sellers. </p>
