<h2 class="header_left_content">Why is there a need for a Nutrient Trading Program?</h2>

<p class="text_left_content">Over the years, pollution levels in the Chesapeake Bay have been increasing. Chief among these pollutants are nutrients, nitrogen and phosphorus. These substances enter the Bay from direct discharges (or point sources), from diffuse sources (nonpoint sources), such as urban and agricultural runoff, as well as atmospheric deposition. </p>

<p class="text_left_content">Much has already been done to reduce these pollutants with the development of <a href="http://www.dnr.state.md.us/bay/tribstrat/">Maryland&rsquo;s Tributary Strategies</a>, but more is still needed. Over the last 15 years, federal, state, and local programs have been developed to assist in mitigating the impacts of pollutants in the Bay; however, the amount of public sector funding required to achieve the desired reductions has fallen short in meeting the goals of a clean Bay. Additionally, once Maryland and other states achieve the necessary reductions, they must maintain that level in order to sustain improved water quality in the Bay.  Population and economic growth in Maryland and other Bay States may, without appropriate policy implementation, lead to increases in nutrient loads because of increases in sewage flows and other nonpoint source flows in urban areas.</p>

