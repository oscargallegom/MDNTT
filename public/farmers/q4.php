<p class="text_left_content">In designing your contracts, you should make sure that you understand the liability issues involved for both you and the buyer.  Much of the demand for agricultural credits will likely come from wastewater treatment plants.  Trades with them will be incorporated into their discharge permits and become permit requirements.  Under the Clean Water Act (CWA), the permit holder will remain solely responsible for meeting the permit requirements and any CWA enforcement action will be taken against it.  This liability cannot be transferred to the credit seller even if it causes a violation by not generating the agreed-upon number of credits.  For this reason, regulated buyers are likely to insist on contract provision that require the seller to cover all of the buyer&rsquo;s financial liability caused by a default by the seller.
</p>

<p class="text_left_content">You can add additional elements and requirements to the contracts to address specific preferences that you may have.  The additional provisions should not conflict with the contractual requirements listed above, however.  Any additional provisions that are not required by the policy need not be submitted with the trade application and can remain confidential if you wish.  It is recommended that you have your proposed contract reviewed by legal counsel before signing it.  
</p>

<p class="text_left_content">
