<p class="text_left_content">Over the years, pollution levels in the Chesapeake Bay have been increasing. Chief among these pollutants are nutrients, nitrogen and phosphorus. These substances enter the Bay from direct discharges (or point sources), from diffuse sources (nonpoint sources), such as urban and agricultural runoff, as well as atmospheric deposition. </p>

